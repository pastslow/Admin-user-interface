var editImgId = document.getElementById("editImgId");
var jobsContainer = document.getElementById("jobsContainer");

$("#jobBtn").click(function(){
    $("#editImgId").toggle();
    $("#userValuesContainer").toggle();
    $("#jobsContainer").toggle();
});

$("#removeBtn").click(function(){
    $("#editImgId").toggle();
    $("#userValuesContainer").toggle();
    $("#jobsContainer").toggle();
});